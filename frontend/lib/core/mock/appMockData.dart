List<MetricPoint> buildMetricSeries({
  required MetricRange range,
  required double base,
  required double step,
}) {
  final count = switch (range) {
    MetricRange.h24 => 12,
    MetricRange.d7 => 7,
    MetricRange.d30 => 10,
    MetricRange.all => 12,
  };

  // spacing between points: 2h for h24/all, 1 day for d7, 3 days for d30
  final spacingHours = switch (range) {
    MetricRange.h24 => 2,
    MetricRange.d7 => 24,
    MetricRange.d30 => 72,
    MetricRange.all => 2,
  };

  return List.generate(count, (index) {
    final factor = index.isEven ? 1 : -1;
    return MetricPoint(
      time: DateTime.now().subtract(
        Duration(hours: (count - index) * spacingHours),
      ),
      value: base + factor * step + (index * 0.12),
    );
  });
}

List<MovementPoint> buildMovementSeries({required MetricRange range}) {
  final count = switch (range) {
    MetricRange.h24 => 12,
    MetricRange.d7 => 7,
    MetricRange.d30 => 10,
    MetricRange.all => 12,
  };

  final spacingHours = switch (range) {
    MetricRange.h24 => 2,
    MetricRange.d7 => 24,
    MetricRange.d30 => 72,
    MetricRange.all => 2,
  };

  final base = switch (range) {
    MetricRange.h24 => 0.8,
    MetricRange.d7 => 4.5,
    MetricRange.d30 => 11.5,
    MetricRange.all => 130.0,
  };

  final wave = switch (range) {
    MetricRange.h24 => 0.28,
    MetricRange.d7 => 1.4,
    MetricRange.d30 => 2.2,
    MetricRange.all => 24.0,
  };

  final drift = switch (range) {
    MetricRange.h24 => 0.05,
    MetricRange.d7 => 0.22,
    MetricRange.d30 => 0.45,
    MetricRange.all => 4.0,
  };

  return List.generate(count, (index) {
    final offset = index - ((count - 1) / 2);
    return MovementPoint(
      time: DateTime.now().subtract(
        Duration(hours: (count - index) * spacingHours),
      ),
      distance_m: double.parse(
        (base + (offset * drift) + (wave * (index.isEven ? 1 : -1)))
            .toStringAsFixed(1),
      ),
    );
  });
}
